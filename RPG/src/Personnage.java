
public class Personnage 
{
	private int life ;
	private int mana ;
	private int attaque ;
	private int attaqueSpe ;
	private String name ;
	
	public Personnage(int life, int mana, int attaque, int attaqueSpe,String name) 
	{
		this.life = life;
		this.mana = mana;
		this.attaque = attaque;
		this.attaqueSpe = attaqueSpe;
		this.name = name;
	}
	
}
